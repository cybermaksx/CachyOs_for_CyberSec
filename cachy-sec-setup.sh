# ──────────────────────────────────────────────────────────────────────────────
# INSTALL AUR HELPER (paru preferred, yay fallback)
# ──────────────────────────────────────────────────────────────────────────────
install_aur_helper() {
    section "AUR Helper"

    if command -v paru &>/dev/null; then
        success "paru already installed."
        return
    fi

    if command -v yay &>/dev/null; then
        success "yay already installed – will use as AUR helper."
        return
    fi

    info "Installing paru..."
    sudo pacman -S --noconfirm --needed base-devel git

    local tmpdir
    tmpdir=$(mktemp -d)
    git clone https://aur.archlinux.org/paru.git "$tmpdir/paru"
    (cd "$tmpdir/paru" && makepkg -si --noconfirm)
    rm -rf "$tmpdir"

    success "paru installed."
}

# ──────────────────────────────────────────────────────────────────────────────
# GENERIC CATEGORY INSTALLER
# Tries pacman first, then AUR helper for anything not found.
# ──────────────────────────────────────────────────────────────────────────────
install_category() {
    local category_name="$1"
    shift
    local tools=("$@")
    local failed=()

    section "Installing: $category_name"

    for pkg in "${tools[@]}"; do
        # Strip inline bash comments
        pkg="${pkg%%#*}"
        pkg="${pkg// /}"
        [[ -z "$pkg" ]] && continue

        if pacman -Qi "$pkg" &>/dev/null; then
            info "$pkg already installed – skipping."
            continue
        fi

        info "Installing $pkg ..."
        if sudo pacman -S --noconfirm --needed "$pkg" 2>/dev/null; then
            success "$pkg installed."
        else
            local aur_helper=""
            command -v paru &>/dev/null && aur_helper="paru"
            command -v yay  &>/dev/null && aur_helper="yay"

            if [[ -n "$aur_helper" ]]; then
                if $aur_helper -S --noconfirm --needed "$pkg" 2>/dev/null; then
                    success "$pkg installed via AUR."
                else
                    warn "$pkg – installation failed, skipping."
                    failed+=("$pkg")
                fi
            else
                warn "$pkg not in repos and no AUR helper available – skipping."
                failed+=("$pkg")
            fi
        fi
    done

    if [[ ${#failed[@]} -gt 0 ]]; then
        warn "Failed in '$category_name': ${failed[*]}"
    fi
}

