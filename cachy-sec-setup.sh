# ──────────────────────────────────────────────────────────────────────────────
usage() {
    cat << 'USAGE'
Usage: cachy-sec-setup.sh [option]

Options:
  --all           Install everything (default)
  --mirrors       BlackArch mirrors only
  --recon         Reconnaissance tools
  --web           Web application testing tools
  --network       Network / MITM tools
  --exploitation  Exploitation & reversing tools
  --passwords     Password attack tools
  --wireless      Wireless audit tools
  --forensics     Forensics & DFIR tools
  --crypto        Crypto & steganography tools
  --se            Social engineering tools
  --cloud         Cloud & container security
  --osint         OSINT tools
  --utils         Developer utilities
  --python        Python security libraries only
  --harden        Kernel sysctl hardening only
  --fish          Fish shell aliases only
  --help          Show this help
USAGE
    exit 0
}

# ──────────────────────────────────────────────────────────────────────────────
# MAIN
# ──────────────────────────────────────────────────────────────────────────────
main() {
    clear
    banner

    local mode="${1:---all}"

    [[ "$mode" == "--help" || "$mode" == "-h" ]] && usage

    preflight
    backup_pacman_conf

    case "$mode" in
        --mirrors)
            add_blackarch_repo
            ;;
        --harden)
            apply_sysctl_hardening
            ;;
        --fish)
            configure_fish
            ;;
        --python)
            install_python_libs
            ;;
        --recon)
            add_blackarch_repo; system_update; install_aur_helper
            install_category "Reconnaissance" "${RECON_TOOLS[@]}"
            ;;
        --web)
            add_blackarch_repo; system_update; install_aur_helper
            install_category "Web Application" "${WEB_TOOLS[@]}"
            ;;
        --network)
            add_blackarch_repo; system_update; install_aur_helper
            install_category "Network" "${NETWORK_TOOLS[@]}"
            ;;
        --exploitation)
            add_blackarch_repo; system_update; install_aur_helper
            install_category "Exploitation" "${EXPLOITATION_TOOLS[@]}"
            ;;
        --passwords)
            add_blackarch_repo; system_update; install_aur_helper
            install_category "Password Attacks" "${PASSWORD_TOOLS[@]}"
            ;;
        --wireless)
            add_blackarch_repo; system_update; install_aur_helper
            install_category "Wireless" "${WIRELESS_TOOLS[@]}"
            ;;
        --forensics)
            add_blackarch_repo; system_update; install_aur_helper
            install_category "Forensics" "${FORENSICS_TOOLS[@]}"
            ;;
        --crypto)
            add_blackarch_repo; system_update; install_aur_helper
            install_category "Crypto & Stego" "${CRYPTO_TOOLS[@]}"
            ;;
        --se)
            add_blackarch_repo; system_update; install_aur_helper
            install_category "Social Engineering" "${SE_TOOLS[@]}"
            ;;
        --cloud)
            add_blackarch_repo; system_update; install_aur_helper
            install_category "Cloud & Container" "${CLOUD_TOOLS[@]}"
            ;;
        --osint)
            add_blackarch_repo; system_update; install_aur_helper
            install_category "OSINT" "${OSINT_TOOLS[@]}"
            ;;
        --utils)
            add_blackarch_repo; system_update; install_aur_helper
            install_category "Utilities" "${UTILITY_TOOLS[@]}"
            ;;
        --all)
            add_blackarch_repo
            system_update
            install_aur_helper

            install_category "Reconnaissance"    "${RECON_TOOLS[@]}"
            install_category "Web Application"   "${WEB_TOOLS[@]}"
            install_category "Network"           "${NETWORK_TOOLS[@]}"
            install_category "Exploitation"      "${EXPLOITATION_TOOLS[@]}"
            install_category "Password Attacks"  "${PASSWORD_TOOLS[@]}"
            install_category "Wireless"          "${WIRELESS_TOOLS[@]}"
            install_category "Forensics"         "${FORENSICS_TOOLS[@]}"
            install_category "Crypto & Stego"    "${CRYPTO_TOOLS[@]}"
            install_category "Social Eng."       "${SE_TOOLS[@]}"
            install_category "Cloud & Container" "${CLOUD_TOOLS[@]}"
            install_category "OSINT"             "${OSINT_TOOLS[@]}"
            install_category "Utilities"         "${UTILITY_TOOLS[@]}"

            install_python_libs
            apply_sysctl_hardening
            create_workspace
            configure_fish
            enable_services
            ;;
        *)
            error "Unknown option: $mode"
            usage
            ;;
    esac

    print_summary
}

main "$@"
