preflight() {
    section "Preflight checks"

    if [[ $EUID -eq 0 ]]; then
        error "Do NOT run as root. The script uses sudo internally."
        exit 1
    fi

    if ! command -v pacman &>/dev/null; then
        error "pacman not found – this script targets CachyOS / Arch-based systems."
        exit 1
    fi

    if ! ping -c1 -W3 archlinux.org &>/dev/null; then
        error "No internet connection detected."
        exit 1
    fi

    success "All preflight checks passed."
}

