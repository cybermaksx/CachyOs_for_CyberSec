# ──────────────────────────────────────────────────────────────────────────────
# ADD BLACKARCH MIRRORS
# ──────────────────────────────────────────────────────────────────────────────
add_blackarch_repo() {
    section "Adding BlackArch repository"

    if grep -q '\[blackarch\]' /etc/pacman.conf; then
        warn "BlackArch repo already present in pacman.conf – skipping."
        return
    fi

    info "Downloading BlackArch strap script..."
    local strap
    strap=$(mktemp /tmp/blackarch-strap.XXXXXX.sh)

    if ! curl -fsSL https://blackarch.org/strap.sh -o "$strap"; then
        error "Failed to download strap.sh"
        rm -f "$strap"
        exit 1
    fi

    # Verify SHA1 checksum published by BlackArch project
    local expected_sha1="5ea40d49ecd14c2e024deecf90605426db97ea0c"
    local actual_sha1
    actual_sha1=$(sha1sum "$strap" | awk '{print $1}')

    if [[ "$actual_sha1" != "$expected_sha1" ]]; then
        error "SHA1 mismatch for strap.sh!"
        error "  Expected : $expected_sha1"
        error "  Got      : $actual_sha1"
        error "Aborting to protect your system."
        rm -f "$strap"
        exit 1
    fi

    chmod +x "$strap"
    sudo bash "$strap"
    rm -f "$strap"

    _write_blackarch_mirrorlist
    success "BlackArch repository added successfully."
}

