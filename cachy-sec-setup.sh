# ──────────────────────────────────────────────────────────────────────────────
# ENABLE SYSTEMD SERVICES
# ──────────────────────────────────────────────────────────────────────────────
enable_services() {
    section "Enabling services"

    local services=(tor docker)

    for svc in "${services[@]}"; do
        if systemctl list-unit-files "${svc}.service" &>/dev/null; then
            if sudo systemctl enable --now "$svc" 2>/dev/null; then
                success "$svc enabled & started."
            else
                warn "Could not enable $svc – it may need manual setup."
            fi
        else
            warn "$svc service unit not found – skipping."
        fi
    done

    if getent group docker &>/dev/null; then
        sudo usermod -aG docker "$USER"
        info "Added $USER to docker group (re-login required)."
    fi
}

