# ──────────────────────────────────────────────────────────────────────────────
# KERNEL / SYSCTL HARDENING
# ──────────────────────────────────────────────────────────────────────────────
apply_sysctl_hardening() {
    section "Kernel hardening (sysctl)"

    sudo tee /etc/sysctl.d/99-cachy-sec.conf > /dev/null << 'SYSCTL'
# ── CachyOS Security Hardening ─────────────────────────────────────────────

# Disable IP forwarding (re-enable only if routing is needed)
net.ipv4.ip_forward = 0
net.ipv6.conf.all.forwarding = 0

# SYN flood protection
net.ipv4.tcp_syncookies = 1

# Ignore ICMP broadcast ping
net.ipv4.icmp_echo_ignore_broadcasts = 1

# Ignore bogus ICMP error responses
net.ipv4.icmp_ignore_bogus_error_responses = 1

# Reverse path filtering (anti-spoofing)
net.ipv4.conf.all.rp_filter = 1
net.ipv4.conf.default.rp_filter = 1

# Reject source-routed packets
net.ipv4.conf.all.accept_source_route = 0
net.ipv4.conf.default.accept_source_route = 0

# Reject ICMP redirects
net.ipv4.conf.all.accept_redirects = 0
net.ipv4.conf.default.accept_redirects = 0
net.ipv6.conf.all.accept_redirects = 0

# Do not send ICMP redirects
net.ipv4.conf.all.send_redirects = 0

# Log packets with impossible addresses (martians)
net.ipv4.conf.all.log_martians = 1

# Disable SUID core dumps
fs.suid_dumpable = 0

# Restrict dmesg to root
kernel.dmesg_restrict = 1

# Hide kernel pointers
kernel.kptr_restrict = 2

# Disable magic SysRq
kernel.sysrq = 0

# Restrict ptrace to parent processes only
kernel.yama.ptrace_scope = 1

# TCP time-wait assassination protection
net.ipv4.tcp_rfc1337 = 1

# Full ASLR
kernel.randomize_va_space = 2
SYSCTL

    sudo sysctl --system &>/dev/null
    success "sysctl hardening applied → /etc/sysctl.d/99-cachy-sec.conf"
}

