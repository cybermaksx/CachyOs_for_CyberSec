create_workspace() {
    section "Creating workspace"
    local base="$HOME/sec"

    mkdir -p \
        "$base/recon"               \
        "$base/web"                 \
        "$base/network"             \
        "$base/exploitation"        \
        "$base/passwords/wordlists" \
        "$base/wireless"            \
        "$base/forensics"           \
        "$base/malware"             \
        "$base/reports"             \
        "$base/ctf"                 \
        "$base/tools/custom"

    # rockyou – try pacman package first, curl as fallback
    local rockyou_dst="$base/passwords/wordlists/rockyou.txt"
    if [[ ! -f "$rockyou_dst" ]]; then
        if [[ -f /usr/share/wordlists/rockyou.txt ]]; then
            cp /usr/share/wordlists/rockyou.txt "$rockyou_dst"
            success "rockyou.txt copied from system wordlists."
        elif [[ -f /usr/share/wordlists/rockyou.txt.gz ]]; then
            gunzip -c /usr/share/wordlists/rockyou.txt.gz > "$rockyou_dst"
            success "rockyou.txt extracted from system wordlists."
        else
            info "Downloading rockyou.txt ..."
            if curl -fsSL \
                "https://github.com/danielmiessler/SecLists/raw/master/Passwords/Leaked-Databases/rockyou.txt.tar.gz" \
                -o "$base/passwords/wordlists/rockyou.txt.tar.gz" 2>/dev/null \
            && tar -xzf "$base/passwords/wordlists/rockyou.txt.tar.gz" \
                   -C "$base/passwords/wordlists/" \
            && rm -f "$base/passwords/wordlists/rockyou.txt.tar.gz"; then
                success "rockyou.txt downloaded."
            else
                warn "Could not download rockyou.txt – grab it manually later."
            fi
        fi
    else
        info "rockyou.txt already present – skipping."
    fi

    success "Workspace ready at $base"
}

